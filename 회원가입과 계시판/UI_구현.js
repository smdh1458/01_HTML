function showmethehint() {
  const hint1 = document.getElementById("hint1");
  hint1.style.display = "block";
}
function showhint() {
  const hint = document.getElementById("hint");
  hint.style.color = "red";
  hint.style.display = "block";
}

function nonehint() {
  const hint = document.getElementById("hint");
  hint.style.display = "none";
}
